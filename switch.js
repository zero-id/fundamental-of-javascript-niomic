function testingSwitch() {
    var nilai = 9

    switch (nilai) {
        case 10:
            console.log("Sangat bagus")
            break
        case 8:
            console.log("Bagus")
            break
        case 6:
            console.log("Cukup")
            break
        default:
            console.log("Kurang")
    }
}

testingSwitch()